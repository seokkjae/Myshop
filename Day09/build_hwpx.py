# ---------- 문단 속성(para_pr) 캐시: (정렬,줄간격,위,아래,좌여백,첫줄) ----------
_pcache = {}
def para_pr(align="JUSTIFY", line=160, before=0, after=4, left=0.0, first=0.0):
    key = (align, line, before, after, left, first)
    if key in _pcache:
        return _pcache[key]
    tmp = doc.add_paragraph("")
    idx = len(doc.paragraphs) - 1
    info = doc.set_paragraph_format(
        paragraph_index=idx, alignment=align, line_spacing_percent=line,
        spacing_before_pt=before, spacing_after_pt=after,
        indent_left_mm=left, first_line_indent_mm=first)
    pid = info["paragraphs"][0]["paraPrIDRef"]
    tmp.remove()
    _pcache[key] = pid
    return pid

# ---------- 글자 속성(char_pr) ----------
def char(size=10.5, bold=False, color=INK):
    return doc.ensure_run_style(size=size, bold=bold, color=color)

# ---------- 본문 문단 추가 ----------
def P(text="", *, c=None, align="JUSTIFY", line=160, before=0, after=5,
      left=0.0, first=0.0, runs=None):
    p = doc.add_paragraph("")
    p.para_pr_id_ref = para_pr(align, line, before, after, left, first)
    if runs:
        for t, kw in runs:
            p.add_run(t, **kw)
    elif text:
        p.add_run(text, char_pr_id_ref=(c if c else char()))
    return p

def spacer(after=6):
    P("", after=after)

def page_break():
    doc.add_paragraph("", pageBreak="1", inherit_style=False)

# ---------- 대제목 / 소제목 ----------
def H1(num, text):
    P(runs=[(f"{num}. ", dict(bold=True, size=15, color=ACCENT2)),
            (text,       dict(bold=True, size=15, color=ACCENT))],
      before=14, after=8)

def H2(text):
    P(text, c=char(12, True, ACCENT2), before=8, after=5)

def lead(text):
    P(text, c=char(10.5, False, INK), after=10)

def note(text):
    P(text, c=char(9, False, SOFT), after=8)

# ---------- 표 ----------
def table(headers, rows, widths=None, first_bold=True, fontsize=9.5):
    ncol = len(headers)
    t = doc.add_table(len(rows) + 1, ncol)
    if widths:
        t.set_column_widths(widths)
    # 머리행
    for c_i, h in enumerate(headers):
        t.set_cell_shading(0, c_i, HEAD_FILL)
        cell = t.cell(0, c_i)
        cp = cell.paragraphs[0]
        cp.clear_text()
        cp.para_pr_id_ref = para_pr("CENTER", 130, 0, 0)
        cp.add_run(h, bold=True, color=WHITE, size=10)
    # 본문행
    for r_i, row in enumerate(rows, start=1):
        for c_i, val in enumerate(row):
            cell = t.cell(r_i, c_i)
            cp = cell.paragraphs[0]
            cp.clear_text()
            is_first = (c_i == 0 and first_bold)
            cp.para_pr_id_ref = para_pr("CENTER" if is_first else "LEFT", 135, 0, 0)
            cp.add_run(str(val), bold=is_first,
                       color=(ACCENT if is_first else INK),
                       size=fontsize)
    spacer(8)
    return t

